// atribuição sempre o simbola aritimetico vem na frente do sinal de = que faz receber
//=recebe
const a = 7
let b = 3

b +=a // atribuição aditiva, equivalente b = b +a
console.log (b)

b-= a // atribução diminutiva, equivalente b=b-a
console.log(b)

b *= a //atribuição multiplicativa, equivalente b = b*a
console.log(b)

b /=a // atubuição divisora, equivale b = b/a
console.log(b)

b %= 2 // atribuição modular, equivale b = b%a
console.log(b)